/**
 * Toast level used for styling and UX.
 */
export enum ToastType {
  /**
   * Success feedback.
   */
  SUCCESS = 'success',
  /**
   * Informational feedback.
   */
  INFO = 'info',
  /**
   * Warning feedback.
   */
  WARNING = 'warning',
  /**
   * Error feedback.
   */
  ERROR = 'error'
}

